package pkg100dayscodingday3;
public class day4Coding {
public static void main(String[] args) {
        // 1. \n = penggunnaan baris baru
        System.out.println("Haii perkenalkan\n nama saya ilda");
        // 2. \t = digunakan untuk tab
        System.out.println("Nama saya: \t ilda riani");
        // 3. \\ = untuk penggunaan backslash (\)
        System.out.println("ilda\\riani");
        // 4. \' = menampilkan tanda kutip tunggal(')
        System.out.println("It\'s ilda");
        // 5. \" = menampilkan tanda kutip ganda
        System.out.println("\"ilda riani\"");
        // 6. \r = mengembalikan ke awal baris
        System.out.println("ilda\r riani");
        // 7. \b = menghilangkan karakter terakhir
        System.out.println("ildaa\b riani");
        // 8. \f = digunakan pada saat memajukan halaman
        System.out.println("ilda \f riani");
    }
}
